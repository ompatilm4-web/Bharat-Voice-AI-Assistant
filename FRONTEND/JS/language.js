/** Apply all [data-i18n] elements on the current page */
function applyTranslations(langCode) {
  const dict = TRANSLATIONS[langCode] || TRANSLATIONS['en'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      // Preserve child elements (e.g. icons inside buttons) тАФ only replace text nodes
      if (el.children.length === 0) {
        el.textContent = dict[key];
      } else {
        // Replace only the first text node
        for (const node of el.childNodes) {
          if (node.nodeType === Node.TEXT_NODE && node.textContent.trim()) {
            node.textContent = dict[key];
            break;
          }
        }
      }
    }
  });

  // Update placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key]) el.placeholder = dict[key];
  });

  // Update html lang attribute
  document.documentElement.lang = langCode;
}

/** Save language and apply across the page */
function setLanguage(langCode) {
  localStorage.setItem('saarthi_lang', langCode);
  applyTranslations(langCode);
  updateSwitcherUI(langCode);

  // Sync voice assistant language selector if present
  const voiceSelect = document.getElementById('language');
  if (voiceSelect) {
    const speechMap = { hi:'hi-IN', en:'en-IN', bn:'bn-IN', te:'te-IN', mr:'mr-IN',
                        ta:'ta-IN', gu:'gu-IN', kn:'kn-IN', ml:'ml-IN', pa:'pa-IN' };
    if (speechMap[langCode]) voiceSelect.value = speechMap[langCode];
  }
}

/** Update the floating switcher button label & active state */
function updateSwitcherUI(langCode) {
  const label = document.getElementById('saarthi-lang-label');
  if (label) label.textContent = CODE_TO_NAME[langCode] || 'English';

  document.querySelectorAll('[data-lang-option]').forEach(btn => {
    const isActive = btn.dataset.langOption === langCode;
    btn.classList.toggle('lang-active', isActive);
  });
}

