window.saarthiTogglePanel = function() {
  const p = document.getElementById('saarthi-lang-panel');
  if (p) p.style.display = p.style.display === 'block' ? 'none' : 'block';
};

window.saarthiSetLang = function(code) {
  setLanguage(code);
  const p = document.getElementById('saarthi-lang-panel');
  if (p) p.style.display = 'none';
};

